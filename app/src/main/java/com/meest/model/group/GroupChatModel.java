package com.meest.model.group;

public class GroupChatModel {

    public String groupName;
    public String groupIcon;
    public String textMessage;
    public String textTime;
    public String textMessageCount;

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getGroupIcon() {
        return groupIcon;
    }

    public void setGroupIcon(String groupIcon) {
        this.groupIcon = groupIcon;
    }

    public String getTextMessage() {
        return textMessage;
    }

    public void setTextMessage(String textMessage) {
        this.textMessage = textMessage;
    }

    public String getTextTime() {
        return textTime;
    }

    public void setTextTime(String textTime) {
        this.textTime = textTime;
    }

    public String getTextMessageCount() {
        return textMessageCount;
    }

    public void setTextMessageCount(String textMessageCount) {
        this.textMessageCount = textMessageCount;
    }
}
